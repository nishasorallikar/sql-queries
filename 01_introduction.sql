-- =========================================================
-- 1. WHAT IS SQL?
-- =========================================================

-- SQL stands for Structured Query Language.

-- SQL is the standard language used to:
-- 1. Create databases and tables
-- 2. Store data
-- 3. Retrieve data
-- 4. Update data
-- 5. Delete data
-- 6. Manage database permissions

-- Example:

SELECT 'Welcome to SQL';

-- =========================================================
-- 2. WHAT IS A DATABASE?
-- =========================================================

-- A database is an organized collection of data.

-- Real-world examples:
-- Banking System
-- Hospital Management System
-- E-Commerce Website
-- School Management System

-- Example Database:
-- EmployeeDB

-- =========================================================
-- 3. WHAT IS A TABLE?
-- =========================================================

-- A table stores data in rows and columns.

-- Example:

/*
+------------+---------------+---------+
| employee_id| employee_name | salary  |
+------------+---------------+---------+
| 101        | Employee_A    | 50000   |
| 102        | Employee_B    | 60000   |
+------------+---------------+---------+
*/

-- =========================================================
-- 4. ROWS AND COLUMNS
-- =========================================================

-- Row    = Single record
-- Column = Attribute/Field

-- Example:

/*
Column Names:
employee_id
employee_name
salary

Row Example:
101 | Employee_A | 50000
*/

-- =========================================================
-- 5. WHAT IS DBMS?
-- =========================================================

-- DBMS = Database Management System

-- It is software used to create and manage databases.

-- Examples:
-- MySQL
-- SQL Server
-- PostgreSQL
-- Oracle
-- SQLite

-- =========================================================
-- 6. WHAT IS RDBMS?
-- =========================================================

-- RDBMS = Relational Database Management System

-- Data is stored in related tables.

-- Examples:
-- MySQL
-- SQL Server
-- PostgreSQL
-- Oracle

-- Features:
-- Tables
-- Relationships
-- Primary Keys
-- Foreign Keys

-- =========================================================
-- 7. SQL VS MYSQL
-- =========================================================

-- SQL:
-- Language used to interact with databases.

-- MySQL:
-- Database software that uses SQL.

-- Example:
-- SQL = English Language
-- MySQL = Person who understands English

-- =========================================================
-- 8. TYPES OF SQL COMMANDS
-- =========================================================

-- SQL commands are divided into 5 categories.

-- 1. DDL (Data Definition Language)
-- CREATE
-- ALTER
-- DROP
-- TRUNCATE

-- 2. DML (Data Manipulation Language)
-- INSERT
-- UPDATE
-- DELETE

-- 3. DQL (Data Query Language)
-- SELECT

-- 4. DCL (Data Control Language)
-- GRANT
-- REVOKE

-- 5. TCL (Transaction Control Language)
-- COMMIT
-- ROLLBACK
-- SAVEPOINT

